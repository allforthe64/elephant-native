import { useState } from 'react';

import { StatusBar } from 'expo-status-bar';
import { StyleSheet, Text, View, TouchableOpacity, Image } from 'react-native';
import { useSafeAreaInsets } from 'react-native-safe-area-context';
import { firebaseAuth } from '../firebaseConfig';

export default function Home({navigation: {navigate}}) {

  //initialize state for loading, instantiate auth object
  const [loading, setLoading] = useState(true)
  const auth  = firebaseAuth

  //if user data is returned from firebaseAuth object, navigate to dashboard screen
  auth.onAuthStateChanged(user => {
    if (user) navigate('Dashboard')
    else setLoading(false)
  })

  const insets = useSafeAreaInsets()

  return (
    <>
      {loading ?
        <View style={{display: 'flex', justifyContent: 'center', alignItems: 'center'}}>
          <Text>Loading...</Text>
        </View>
      :
        <View style={{
          flex: 1,
          backgroundColor: 'rgb(0, 0, 0)',
          alignItems: 'center',
          justifyContent: 'center',
          paddingTop: insets.top,
          paddingBottom: insets.bottom
        }}>
          <Image source={require('../assets/elephant_bg.jpg')} style={styles.bgImg}/>
          <View style={styles.modal}>
            <Text style={styles.bigHeader}>Welcome to My Elephant App</Text>
            <Text style={styles.subheading}>Take control of your personal data</Text>
            <View style={styles.buttonContainer}>
              <TouchableOpacity style={styles.button} onPress={() => navigate('Sign In/Sign Up')}>
                  <Text style={styles.buttonText}>Sign In/Sign Up</Text>
              </TouchableOpacity>
              <TouchableOpacity style={styles.button} onPress={() => navigate('About')}>
                  <Text style={styles.buttonText}>More About Us</Text>
              </TouchableOpacity>
            </View>
            <Text style={styles.tagline}>*A portion of all proceeds generated by My Elephant App will be donated to saving endangered Elephants around the globe</Text>  
          </View>
          <StatusBar style="auto" />
        </View>
      }
    </>
  );
}

const styles = StyleSheet.create({
  bgImg: {
    objectFit: 'scale-down',
    opacity: .25
  },
  modal: {
    width: '90%',
    height: '60%',
    backgroundColor: 'rgba(0, 0, 0, .65)',
    paddingTop: '20%',
    paddingBottom: 10,
    position: 'absolute',
    display: 'flex',
    flexDirection: 'column',
    alignItems: 'center'
  },
  bigHeader: {
    color: 'white',
    fontSize: 24,
    textAlign: 'center',
    fontWeight: '700',
    marginBottom: '2.5%'
  },
  subheading: {
    color: 'white',
    textAlign: 'center',
    fontSize: 16,
    marginBottom: '6%'
  },
  buttonContainer: {
    flex: 1,
    alignItems: 'center',
    width: '100%'
  },
  button: {
    width: '60%',
    borderColor: '#777',
    borderRadius: 25,
    backgroundColor: 'white',
    borderWidth: 1,
    paddingTop: '2%',
    paddingBottom: '2%',
    marginTop: '5%',
  },
  buttonText: {
    textAlign: 'center',
    fontSize: 14,
    width: '100%',
  },
  tagline: {
    color: 'white', 
    textAlign: 'center', 
    width: '90%', 
    marginBottom: '15%'
  }
});
